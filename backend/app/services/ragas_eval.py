"""
RAGAS Evaluation Module for RAG Systems

RAGAS (RAG Assessment) metrics using LLM-as-judge:
- Faithfulness: Does the answer match the retrieved context?
- Answer Relevancy: Is the answer relevant to the question?
- Context Precision: Are retrieved chunks high-quality for the query?
- Context Recall: Did we retrieve all information needed?

Reference: https://github.com/explodinggradients/ragas
"""

from typing import List, Dict, Any

from datasets import Dataset

from ragas import evaluate
from ragas.metrics import (
    faithfulness,
    answer_relevancy,
    context_precision,
    context_recall,
)

from core.config import OPENAI_API_KEY
from app.services.retriever import query_documents

def _build_ragas_dataset(eval_set: List[Dict[str, Any]]) -> Dataset:
    """
    Convert our eval set to RAGAS dataset format.

    RAGAS expects:
    - question: str
    - answer: str (generated by RAG)
    - contexts: List[str]
    - ground_truths: List[str] (reference answers)
    """
    questions = []
    answers = []
    contexts = []
    ground_truths = []

    for item in eval_set:
        question = item["query"]

        # Get RAG response
        answer, sources = query_documents(question, session_id="ragas-eval")

        # Get context from sources
        ctx_list = [s["snippet"] for s in sources]

        questions.append(question)
        answers.append(answer)
        contexts.append(ctx_list)
        ground_truths.append(item.get("ground_truth", ""))

    return Dataset.from_dict({
        "question": questions,
        "answer": answers,
        "contexts": contexts,
        "ground_truths": ground_truths,
    })


def run_ragas_eval(eval_set: List[Dict[str, Any]]) -> Dict[str, float]:
    """
    Run RAGAS evaluation on the RAG system.

    Returns dict with metrics:
    - faithfulness
    - answer_relevancy
    - context_precision
    - context_recall
    """
    if not OPENAI_API_KEY:
        raise ValueError("OPENAI_API_KEY not set - RAGAS requires it for LLM evaluation")

    print("Building RAGAS dataset...")
    dataset = _build_ragas_dataset(eval_set)

    print("Running RAGAS evaluation (LLM-as-judge)...")
    print("This will call OpenAI API for each metric per query.\n")

    return evaluate(
        dataset,
        metrics=[
            faithfulness,
            answer_relevancy,
            context_precision,
            context_recall,
        ],
        raise_exceptions=False,
    )


def print_ragas_report(scores: Dict[str, Any]) -> None:
    """Print formatted RAGAS evaluation report."""
    print("\n" + "=" * 60)
    print("RAGAS Evaluation Report (RAG System)")
    print("=" * 60)

    scores_dict = scores if isinstance(scores, dict) else scores.to_dict()

    for metric, value in scores_dict.items():
        if isinstance(value, (int, float)):
            print(f"  {metric:<25}: {value:.4f}")

    print("=" * 60)
    print("\nMetric Descriptions:")
    print("  Faithfulness      : Does answer match the retrieved context?")
    print("  Answer Relevancy : Is answer relevant to the question?")
    print("  Context Precision : Are retrieved chunks high-quality?")
    print("  Context Recall    : Did we retrieve all needed information?")
    print("=" * 60 + "\n")
